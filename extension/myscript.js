function readQuestion() {
    console.log("readQuestion")

    var question = $('.game-question__text')
    if (question.length == 0) {
        console.log("no Question")
        return
    }

    let questionText = question.text().trim();
    console.log(questionText)

    var ansIndex = 'no';
    var BreakException = {};
    try {
        ans.forEach(function (item, index) {
            if (item.question == questionText) {
                console.log("ans: " + item.answer)
                ansIndex = item.ansCode
                throw BreakException
            }
        })
    } catch (e) {
        if (e !== BreakException) throw e;
    }

    let ansButtons = $('.btn.btn__blue')
    
    ansButtons.each(function (index) {
        $(this).css('background-color', '');
    });

    ansButtons.each(function (index) {
        if (index == ansIndex - 1) {
            $(this).css('background-color', 'red');
        }
        console.log(index + ": " + $(this).text());
    });
}


